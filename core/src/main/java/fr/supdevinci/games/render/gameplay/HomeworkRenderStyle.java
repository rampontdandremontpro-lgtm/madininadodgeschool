package fr.supdevinci.games.render.gameplay;

public class HomeworkRenderStyle {

    private final float scaleX;
    private final float scaleY;

    public HomeworkRenderStyle(float scaleX, float scaleY) {
        this.scaleX = scaleX;
        this.scaleY = scaleY;
    }

    public float getScaleX() {
        return scaleX;
    }

    public float getScaleY() {
        return scaleY;
    }
}